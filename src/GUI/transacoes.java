         public ArrayList <transacao> transacao_info(int id_bar) {
         ArrayList <transacao>transacao_info = new ArrayList<>();
         try{
        Connection con = ConnectionProvider.Connector();
        String query ="SELECT info_stock.id_produto as id_produto,nome as nome,data, tempo,qtd from info_stock inner join produto on info_stock.id_produto=produto.id_produto where id_bar=? ";
        PreparedStatement pst= con.prepareStatement(query);
        pst.setInt(1,id_bar);
        ResultSet rs=pst.executeQuery();
         
        
      transacao t;
        while(rs.next()){
            t=new transacao(rs.getInt("id_produto"),rs.getString("nome"),rs.getString("data"),rs.getString("tempo"),rs.getInt("qtd"));
           transacao_info.add(t);
        }
         }catch(Exception e){
             JOptionPane.showMessageDialog(null, e);
         }
         return transacao_info;
    }
         
         public void mostrar_transacao_info(int id_bar){//distribuir info da tabela mesa na jtable
        ArrayList<transacao> lista =transacao_info(id_bar);
        DefaultTableModel model =(DefaultTableModel)transacoes_tabela.getModel();
        Object[]  row= new Object [5];
        for (int i = 0; i < lista.size(); i++) {
            row[0]=lista.get(i).getId();
            row[1]=lista.get(i).getNome();
            row[2]=lista.get(i).getData();
            row[3]=lista.get(i).getTempo();
            row[4]=lista.get(i).getQtd();
            model.addRow(row);
            
        }
            }
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
