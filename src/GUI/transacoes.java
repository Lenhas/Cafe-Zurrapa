    private void ver_faturas_butaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ver_faturas_butaoActionPerformed
        try{

            String data = jTextField1.getText();
            if(data.equalsIgnoreCase("")){

                DefaultTableModel dm1 = (DefaultTableModel)transacoes_tabela.getModel();
                while(dm1.getRowCount() > 0)
                {
                    dm1.removeRow(0);
                }
                mostrar_transacao_info(id_bar);
                
                Connection con = ConnectionProvider.Connector();
                String query1 ="select sum(preco) as preco from info_stock inner join produto on info_stock.id_produto=produto.id_produto where id_bar=?";
                PreparedStatement pst1= con.prepareStatement(query1);
                pst1.setInt(1,id_bar);
                ResultSet rs1=pst1.executeQuery();
                while(rs1.next()){
                    despesas=rs1.getDouble("preco");
                }
                despesasS=Double.toString(despesas);
                lucro_total_label.setText(despesasS+"€");
                con.close();
                pst1.close();

            }else{

                DefaultTableModel dm2 = (DefaultTableModel)transacoes_tabela.getModel();
                while(dm2.getRowCount() > 0)
                {
                    dm2.removeRow(0);
                }
                 mostrar_transacao_info(id_bar);
                Connection con2 = ConnectionProvider.Connector();
                String query2 ="select sum(preco) as preco from   info_stock inner join produto on info_stock.id_produto=produto.id_produto  where id_bar=? and data=?";
                PreparedStatement pst2= con2.prepareStatement(query2);
                pst2.setInt(1,id_bar);
                pst2.setString(2,data);
                ResultSet rs2=pst2.executeQuery();
                while(rs2.next()){
                    despesas=rs2.getDouble("preco");
                }
                despesasS=Double.toString(despesas);
                lucro_total_label.setText(despesasS+"€");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);

        }
    }//GEN-LAST:event_ver_faturas_butaoActionPerformed

